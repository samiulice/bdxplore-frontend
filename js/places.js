/**
 * Generates a responsive tourist info section with image on left and details on right.
 * @param {string} id - Spot ID (e.g. "ahsan_manzil")
 * @param {HTMLElement} container - Target container element
 */
function generateTouristInfo(id, container) {
  const spot = places.touristSpots.find((s) => s.id === id);
  if (!spot || !container) {
    container.innerHTML = `
    <div class="spot-wrapper">
      <div class="spot-details text-center">
        <p><strong>এই স্থানের তথ্য পাওয়া যায়নি।</strong></p>
      </div>
    </div>
    `;
    return;
  }

  container.innerHTML = `
    <div class="spot-wrapper">
      <div class="spot-image">
        <img style="object-fill: contain;" src="${
          spot.image || "img/saint-martin.jpg"
        }" alt="${spot.name}">
      </div>
      <div class="spot-details">
        <h2>${spot.name}</h2>
        <p><strong>অবস্থান:</strong> ${spot.location}</p>
        ${
          spot.description
            ? `<p><strong>বিবরণ:</strong> ${spot.description}</p>`
            : ""
        }
        ${spot.history ? `<p><strong>ইতিহাস:</strong> ${spot.history}</p>` : ""}
        ${
          spot.architecture
            ? `<p><strong>স্থাপত্য:</strong> ${spot.architecture}</p>`
            : ""
        }
        ${
          spot.features
            ? `<p><strong>বৈশিষ্ট্য:</strong> ${spot.features}</p>`
            : ""
        }
        ${
          spot.current_status
            ? `<p><strong>বর্তমান অবস্থা:</strong> ${spot.current_status}</p>`
            : ""
        }

        ${
          spot.attractions?.length
            ? `
          <h4>দর্শনীয় স্থানসমূহ:</h4>
          <ul>${spot.attractions.map((a) => `<li>${a}</li>`).join("")}</ul>
        `
            : ""
        }

        <h4>ভ্রমণ তথ্য:</h4>
        <p><strong>প্রবেশ মূল্য:</strong> ${spot.visit_info.entry_fee}</p>
        <p><strong>সময়সূচি:</strong> ${spot.visit_info.open_hours}</p>

        <p><strong>যেভাবে যাবেন:</strong> ${spot.how_to_go}</p>
        <p><strong>থাকার ব্যবস্থা:</strong> ${spot.where_to_stay}</p>
        ${spot.food ? `<p><strong>খাবার:</strong> ${spot.food}</p>` : ""}
        ${
          spot.nearby_places?.length
            ? `<p><strong>কাছাকাছি স্থান:</strong> ${spot.nearby_places.join(
                ", "
              )}</p>`
            : ""
        }
        ${spot.tips ? `<p><strong>টিপস:</strong> ${spot.tips}</p>` : ""}
      </div>
    </div>
  `;
}
